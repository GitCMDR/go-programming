package main

// Using the short declaration operator, ASSIGN these VALUES to VARIABLES with the
// IDENTIFIERS "x", "y" and "z"

import "fmt"


func main() {
	x := 42
	y := "James Bond"
	z := true

	// Print them all using a single print statements
	fmt.Println(x, y, z)

	// Print them all using three separate print statements
	fmt.Println(x)
	fmt.Println(y)
	fmt.Println(z)

}



